.global _Reset
_Reset:
    ldr x30, =stack_top	                    // setup stack
    mov sp, x30                             // load stack pointer
    ldr x0, =_vector_table                  // load vtable into r0
    msr VBAR_EL1, x0
    isb              // move r0 to base vector table register
    bl _entry                               // go to zig entry point
    b .                                     // hang forever


.section .vectors, "ax"
.align 11                // 2KB alignment required
.global _vector_table
_vector_table:

// ========== Exceptions from EL0 ==========
el0_sync:
    // --- Dump registers --- 
    stp     x19, x20, [sp, #-16]!
    stp     x21, x22, [sp, #-16]!
    stp     x23, x24, [sp, #-16]!
    stp     x25, x26, [sp, #-16]!
    stp     x27, x28, [sp, #-16]!
    stp     x29, x30, [sp, #-16]!

    b el0_sync_handler

    // --- Restore registers ---
    ldp     x29, x30, [sp], #16
    ldp     x27, x28, [sp], #16
    ldp     x25, x26, [sp], #16
    ldp     x23, x24, [sp], #16
    ldp     x21, x22, [sp], #16
    ldp     x19, x20, [sp], #16
    
    eret    
    .space 128 - (. - el0_sync)

